/*

Given a number n, check whether it is even or odd. Return true for even and false for odd.

Examples: 

Input: 2 
Output: true


Input: 5
Output: false

*/

function isEven(n){
    if (n % 2 == 0){
        return "true"
    } 
    else{
        return "false"
    }
}

